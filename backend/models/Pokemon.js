const Sequelize = require('sequelize')
const connection = require('../database/db')


const Pokemon = connection.define('poke', {
    name: {
        type: Sequelize.STRING,
        allowNull: false
    },
    type: {
        type: Sequelize.STRING,
        allowNull: false
    },
    description: {
        type: Sequelize.STRING,
        allowNull: false
    },
    image: {
        type: Sequelize.STRING,
        allowNull: false
    }
})



module.exports = Pokemon